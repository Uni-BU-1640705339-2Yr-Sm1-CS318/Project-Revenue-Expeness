    private void jbtnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnaddActionPerformed
        String namein = jtfname.getText();
        String amountin = jtfamount.getText();
        String memoin = jtfmemo.getText();
        
        if (namein.equals("")){
            JOptionPane.showMessageDialog(null, "Please Input Name");
            jlresult.setText("Name must not be null");
        } else if (amountin.equals("")){
            JOptionPane.showMessageDialog(null, "Please Input Amount");
            jlresult.setText("Amount must not be null");
        } else if (memoin.equals("")){
            JOptionPane.showMessageDialog(null, "Please Input Memo");
            jlresult.setText("Memo must not be null");
        } else {
        
            double amount = Double.parseDouble(amountin);
        
            if (jtfname == null){
                JOptionPane.showMessageDialog(null, "Please Write name");
            }
        
            if (jrdrevenue.isSelected()){
                revenuename.add(namein);
                revenueamount.add(amount);
                revenuememo.add(memoin);
                String type = "revenue";
            
                String[] data = {namein,Double.toString(amount),memoin,type};
                DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
                tblModel.addRow(data);

            } else if (jrdexpeness.isSelected()) {
                expenessname.add(namein);
                expenessamount.add(amount);
                expenessmemo.add(memoin);
                String type = "expeness";
            
                String[] data = {namein,Double.toString(amount),memoin,type};
                DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
                tblModel.addRow(data);
            } else {
                JOptionPane.showMessageDialog(null, "Please Select Revenue or Expenness radio button");
            }
            jlresult.setText("Add to table success");
        }
    }//GEN-LAST:event_jbtnaddActionPerformed

