    private void jbtncalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtncalculateActionPerformed
        if (jrdrvep.isSelected()){
            double sumrv = 0;
            for (double numrv : revenueamount){
                sumrv += numrv;
            }
            double sumep = 0;
            for (double numep : expenessamount){
                sumep += numep;
            }
            double sum = sumrv - sumep;
            //System.out.println(sum);
            jlbal.setText(Double.toString(sum) + " Bath");
            jlresult.setText("Caculate complete");
        } else if (jrdrv.isSelected()) {
            double sumrv = 0;
            for (double numrv : revenueamount){
                sumrv += numrv;
            }
            jlbal.setText(Double.toString(sumrv) + " Bath");
            jlresult.setText("Caculate complete");
        } else if (jrdep.isSelected()) {
            double sumep = 0;
            for (double numep : expenessamount){
                sumep += numep;
            }
            jlbal.setText(Double.toString(sumep) + " Bath");
            jlresult.setText("Caculate complete");
        } else {
            JOptionPane.showMessageDialog(null, "Please Select value to caculate");
            jlresult.setText("User have to select type of caculate");
        }
    }//GEN-LAST:event_jbtncalculateActionPerformed

